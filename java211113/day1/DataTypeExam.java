package java211113.day1;

public class DataTypeExam {

	public static void main(String[] args) {
		
		
		System.out.println("100"); // 숫자 X, 문자 O
		
		System.out.println(100); // 숫자 O, 문자 X
		
		System.out.println(100 + 100); // 산술연산 더하기
		
		
		// 곱하기 : *, 빼기 : -, 나누기 : /, 나머지 : %
		
		System.out.println(100 + 200);
		System.out.println(10 * 10);
		System.out.println(1000 - 200);
		System.out.println(1000 / 200);
		System.out.println(10 % 3);
		
		// 결론 : 똑같이 보이더라도 값을 구별해서 사용해야된다. 값에는 유형이 있다. 자료형(데이터 타입)이라고 부른다.
		
		
		
		
	}

}

