package java211113.day1;

public class VariableExam {

	public static void main(String[] args) {

		
		// 변수 - 값(데이터)를 저장
		// 변수를 만들 때 필요한 것 -> 그릇의 모양 및 크기, 그릇의 이름
		int age; // 숫자 -> int, 이름
		
		// 변수에 값을 저장하기
		age = 21; // 오른쪽 값을 왼쪽 변수에 저장
		
		System.out.println(age);
		System.out.println(age);
		System.out.println(age);
		
		String name; // 문자 -> String		
		name = "차태현";
		
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		

		// 결론 : 변수는 값을 저장하는 그릇. 
		// 왜 사용할까?
		// 1. 하나의 데이터를 계속 재활용할 수 있다. 같은 데이터를 여러번 선택할 수 있다.
		// 2. 데이터에 명확한 의미 부여. 
		
		
		
	}

}
